#Arithmetic Operators
#Arithmetic operators are used with numeric values to perform common mathematical operations:
#Operator   	Name	        Description                                         Example	Try it          Output
#   +	     Addition	        Adds two operands	                                    x + y	    10 + 5	    15
#   -	     Subtraction        Subtracts second operand from the first	                x - y	    10 - 5	    5
#   *	     Multiplication	    Multiplies two operands	                                x * y	    10 * 5	    50
#   /	     Division	        Divides first operand by the second	                    x / y	    10 / 5	    2
#   %	     Modulus	        Returns the remainder of the division	                x % y	    10 % 5	    0
#   **      Exponentiation	    Performs exponential (power) calculation on operators	x ** y	10 ** 5	    100000
#   //      Floor division -     
#       aka integer division    	Returns the quotient of the division	            x // y	10 // 5	    2
