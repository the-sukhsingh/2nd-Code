#Comparison or Relational Operators
#Comparison or Relational operators are used to compare values. It either returns True or False according to the condition.
#Operator	 Description    Name	    Example	Try it	    Output	
#   ==	     Equal to	    x == y	       5 == 3	        False
#   !=	     Not equal	    x != y	       5 != 3	        True
#   >	     Greater than	x > y	       5 > 3	        True
#   <	     Less than	    x < y	       5 < 3	        False
#   >=	     Greater than or equal to	x >= y	5 >= 3	True
#   <=	     Less than or equal to	x <= y	5 <= 3	False